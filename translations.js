let currentLang = localStorage.getItem('chahan_lang') || 'en';

function applyLang(lang) {
  currentLang = lang;
  localStorage.setItem('chahan_lang', lang);
  const t = T[lang];
  // Update static UI
  UI_MAP.forEach(m => {
    const el = document.querySelector(m.s);
    if (el) { m.h ? el.innerHTML = t[m.k] : el.textContent = t[m.k]; }
  });
  // Nav links
  const navA = document.querySelectorAll('.nav-links a');
  if (navA[0]) navA[0].textContent = t.nav_home;
  if (navA[1]) navA[1].textContent = t.nav_collection;
  if (navA[2]) navA[2].textContent = t.nav_about;
  if (navA[3]) navA[3].textContent = t.nav_order;
  // Filter tabs
  const tabs = document.querySelectorAll('.tab');
  const tabKeys = ['f_all','f_bridal','f_silk','f_party','f_casual'];
  tabs.forEach((tb,i) => { if (tabKeys[i]) tb.textContent = t[tabKeys[i]]; });
  // WA button text in hero
  const waBtn = document.querySelector('.btn-outline-wa');
  if (waBtn) { const svg = waBtn.querySelector('svg'); waBtn.textContent = ''; if(svg) waBtn.prepend(svg); waBtn.append(' '+t.btn_wa); }
  // Nav WA
  const navWa = document.querySelector('.nav-wa');
  if (navWa) { const svg = navWa.querySelector('svg'); navWa.textContent=''; if(svg) navWa.prepend(svg); navWa.append(' '+t.nav_wa); }
  // Lang buttons active
  document.querySelectorAll('.lang-btn').forEach(b => b.classList.toggle('active', b.dataset.lang===lang));
  // Re-render products
  if (typeof renderProducts === 'function') {
    const activeTab = document.querySelector('.tab.active');
    renderProducts(activeTab ? activeTab.dataset.filter : 'all');
  }
}

// Init lang switcher
document.querySelectorAll('.lang-btn').forEach(btn => {
  btn.addEventListener('click', () => applyLang(btn.dataset.lang));
});
